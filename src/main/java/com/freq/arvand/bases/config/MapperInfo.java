package com.freq.arvand.bases.config;

public class MapperInfo {
    private String className;
    private String classPackage;
    private String xmlFileName;
    private String xmlFilePackage;

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getClassPackage() {
        return classPackage;
    }

    public void setClassPackage(String classPackage) {
        this.classPackage = classPackage;
    }

    public String getXmlFileName() {
        return xmlFileName;
    }

    public void setXmlFileName(String xmlFileName) {
        this.xmlFileName = xmlFileName;
    }

    public String getXmlFilePackage() {
        return xmlFilePackage;
    }

    public void setXmlFilePackage(String xmlFilePackage) {
        this.xmlFilePackage = xmlFilePackage;
    }
}

